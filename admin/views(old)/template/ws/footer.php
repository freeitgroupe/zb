    function loadcart() {
        var ob_1 = {'res': '1'};
        $.ajax({
            type: "POST",
            url: "/category/1",
            dataType: "json",
            data: "param_res=" + JSON.stringify(ob_1),
            cache: false,
            success: function (data) {
                //console.log(data['count']);
               // console.log(data['int']);
                //console.log(data);

                if (data == "0") {
                    $(".jqcart-cart-label > span").html("0");
                    $(".summ-cart").html('');

                    if(window.location.href == "<?=PATH .'cart'?>" ){
                        $('.button-order').fadeOut(100);
                        $('.block-confirm').fadeOut(200);
                        $('.cart-zero').fadeIn(200);
                    }

                } else {

                    $.each(data['id_select_product'], function( index, value ) {
                        event_deal_btn = $('button').filter($( "[data-id =" + value + "]" ));
                        event_deal =  event_deal_btn.children('.fa');
                        event_deal_name = $('button').filter($( "[data-id =" + value + "]" )).children('.name-for-button').replaceWith('В корзине');
                        if( event_deal.hasClass("fa-shopping-cart")){
                            event_deal_btn.prop('disabled', true);
                            event_deal_btn.removeClass("cart");
                            event_deal_btn.addClass("disabled");
                            event_deal.removeClass("fa-shopping-cart");
                            event_deal.addClass("fa-check btn-disabled");
                        }
                        if((window.location.href).indexOf("product") > 0){
                            event_deal_input = $('input').filter($( "[data-product =" + value + "]" ));
                            var res_additions = event_deal_input.prop('checked', true);
                            additions_product();
                        }
                        //console.log();


                        //console.log( index + ": " + value);
                    });

                    $(".jqcart-cart-label > span").html(data['count']);
                    $(".summ-cart").html(' '+(accounting.formatNumber(data['int'], 2, " ", ",")) + "<?= $currency?>");
                    $('.cart-zero').hide();
                    $('.building-price').html(' '+(accounting.formatNumber(data['int'], 2, " ", ",")) + "<?= $currency?>");

                }

            }
        });

    }

